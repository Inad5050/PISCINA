char	*ft_strupcase(char *str)
{
	char	*ptr;

	ptr = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str -32;
		}
		str++;
	}
	return (ptr);
}
